// 1. Operações Aritméticas Básicas
// - Soma: Adiciona dois ou mais valores.
//   Exemplo: total = preco1 + preco2 + preco3;
// - Subtração: Subtrai um valor de outro.
//   Exemplo: valorDescontado = precoOriginal - desconto;
// - Multiplicação: Multiplica dois ou mais valores.
//   Exemplo: custoTotal = precoUnitario * quantidade;
// - Divisão: Divide um valor por outro.
//   Exemplo: media = (nota1 + nota2 + nota3) / 3;
// - Módulo: Obtém o resto da divisão de um valor por outro.
//   Exemplo: if (numero % 2 == 0) { ... } // Verifica se o número é par.

// 2. Operações de Comparação
// - Igualdade (==): Verifica se dois valores são iguais.
//   Exemplo: if (senha == senhaCorreta) { ... }
// - Diferença (!=): Verifica se dois valores são diferentes.
//   Exemplo: if (numero != valorProcurado) { ... }
// - Maior que (>): Verifica se um valor é maior que outro.
//   Exemplo: if (salario > limite) { ... }
// - Menor que (<): Verifica se um valor é menor que outro.
//   Exemplo: if (preco < orcamento) { ... }
// - Maior ou igual a (>=): Verifica se um valor é maior ou igual a outro.
//   Exemplo: if (idade >= 18) { ... }
// - Menor ou igual a (<=): Verifica se um valor é menor ou igual a outro.
//   Exemplo: if (quantidade <= limite) { ... }

// 3. Operações Lógicas
// - E Lógico (&&): Verifica se ambas as condições são verdadeiras.
//   Exemplo: if (idade >= 18 && usuarioAtivo) { ... }
// - Ou Lógico (||): Verifica se pelo menos uma das condições é verdadeira.
//   Exemplo: if (numero >= 0 || numero == 0) { ... }
// - Negação (!): Negar uma condição.
//   Exemplo: if (!usuarioBloqueado) { ... }

// 4. Cálculos Específicos
